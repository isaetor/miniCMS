const inappropriateWordsHash = new Set([
    "0x6a0x73", // کص
    "0x6b0x69", // کیر
    "0x6b0x76", // کون
    "0x670x68", // گه
    "0x6b0x690x72", // کیر
    "0x6b0x760x6e", // کون
    "0x670x690x64", // گید
    "0x670x680x69", // گهی
    "0x660x750x63", // fuck
    "0x730x680x69", // shit
    "0x620x690x74", // bit
    "0x610x730x73", // ass
    "0x640x690x63", // dic
    "0x700x750x73", // pus
    "0x630x6f0x63", // coc
    "0x630x750x6e"  // cun
]);

export const containsInappropriateContent = (text: string): boolean => {
    const textHash = text.toLowerCase().split('').map(char => 
        '0x' + char.charCodeAt(0).toString(16)
    ).join('');

    return Array.from(inappropriateWordsHash).some(word => 
        textHash.includes(word)
    );
}; 